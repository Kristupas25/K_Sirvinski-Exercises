let bitNumberOne = 10;
let bitNumberTwo = 20;

// 3. console.log(bitNumberOne & bitNumberTwo); // 0
console.log("3. Bitwise AND:", bitNumberOne & bitNumberTwo);

// 4. console.log(bitNumberOne | bitNumberTwo); // 30
console.log("4. Bitwise OR:", bitNumberOne | bitNumberTwo);

// 5. console.log(~bitNumberTwo); // -21
console.log("5. Bitwise NOT:", ~bitNumberTwo);

// 6. console.log(bitNumberOne ^ bitNumberTwo); // 30
console.log("6. Bitwise XOR:", bitNumberOne ^ bitNumberTwo);

// 7. console.log(bitNumberOne << 1); // 20
console.log("7. Bitwise Left Shift:", bitNumberOne << 1);

// 8. console.log(bitNumberOne << 5); // 320
console.log("8. Bitwise Left Shift:", bitNumberOne << 5);

// 9. console.log(bitNumberOne >> 1); // 5
console.log("9. Bitwise Right Shift:", bitNumberOne >> 1);

// 10. console.log(bitNumberOne >>> 1); // 5
console.log("10. Bitwise Unsigned Right Shift:", bitNumberOne >>> 1);

// 11. console.log((-100 >>> 3).toString(2)); // 111111111111111111111111111111111111111111111111111111111111001
console.log("11. Bitwise Unsigned Right Shift:", (-100 >>> 3).toString(2));

// 12. console.log(true << 1); // 2
console.log("12. Bitwise Left Shift:", true << 1);

// 13. console.log(false << 1); // 0
console.log("13. Bitwise Left Shift:", false << 1);

// 14. console.log(true >> 1); // 0
console.log("14. Bitwise Right Shift:", true >> 1);

// 15. console.log(false >> 1); // 0
console.log("15. Bitwise Right Shift:", false >> 1);

// 16. console.log(~false); // -1
console.log("16. Bitwise NOT:", ~false);

// 17. console.log(~true); // -2
console.log("17. Bitwise NOT:", ~true);